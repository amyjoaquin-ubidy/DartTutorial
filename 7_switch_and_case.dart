
void main() {

	// Switch Case Statements: Applicable for only 'int' and 'String'

	String grade = 'A';

	switch (grade) {

		case 'A':
			print("perfect grade of A");
			break;

		case 'B':
			print("amazing !");
			break;

		case 'C':
			print("damn");
			break;

		case 'F':
			print("oh no");
			break;
		default:
			print("Invalid Grade");
	}
}
